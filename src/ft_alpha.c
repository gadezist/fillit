#include "fillit.h"

int		ft_alpha(char *str, int c, int i)
{
	if ((c <= 'Z' && c >= 'A') && ((str[i - 1] == c) || (str[i + 1] == c) ||
		(str[i - 5] == c) || (str[i + 5] == c)))
		return (1);
	else
		return (0);
}
